// type of operators 

// data type 
// string "gourav"
// number 2,4,5.6
// boolean
// undefined 
// null 
// BigInt
// symbol 

let age = 30;
let firstName = "Gourav";

console.log(typeof age);
console.log(typeof firstName);

// convert number to string
age = age + ""; 
console.log(typeof age);

// convert string to number 
// include '+' in front of the string 

let mStr = +"34";
console.log(mStr);
console.log(typeof mStr);


