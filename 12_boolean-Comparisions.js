// boolean and comparison operator

// boolean (true, false)

let num1 = "2";
let num2 = 5;
console.log(num1<num2);
console.log(num1>num2);

// check equality 
console.log(num1 == num2); 

console.clear();
// == vs ===
num1 = "7";
num2 = 7;
console.log(num1 == num2 );     //true // check only the values
console.log(num1 === num2 );    //false // check the datatype as well


// != vs !==
// !== (it will check the comaprison and the type of variable)

console.log(num1 != num2 );     // false // both values equals
console.log(num1 !== num2 );    // true  // both values equals "7" != 7
